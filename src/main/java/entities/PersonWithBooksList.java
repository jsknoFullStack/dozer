package entities;

import java.util.List;

public class PersonWithBooksList {

    private String name;
    private Integer age;
    private List<Book> books;

    public PersonWithBooksList() {
    }

    public PersonWithBooksList(String name, Integer age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public List<Book> getBooks() {
        return books;
    }

    public void setBooks(List<Book> books) {
        this.books = books;
    }
}
